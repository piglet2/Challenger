package com.envision.envservice.validator.exception;

/**
 * @ClassName RuleBeanExeception
 * @author guowei.wang
 * @date 2015-11-10
 */
public class RuleBeanExeception extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public RuleBeanExeception() {
		super();
	}

	public RuleBeanExeception(String message) {
		super(message);
	}

	public RuleBeanExeception(Throwable cause) {
		super(cause);
	}
	
	public RuleBeanExeception(String message, Throwable cause) {
		super(message, cause);
	}
	
	public RuleBeanExeception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
