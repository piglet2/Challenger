package com.envision.envservice.entity.dto;

import java.util.Date;

/**
 * @ClassName UserCase
 * @author guowei.wang
 * @date 2016-1-4
 */
public class UserCase {

	private Integer id;
	
	private String members;
	
	private byte[] message;
	
	private Integer top;
	
	private Boolean hasRead;
	
	private Integer newPrise;
	
	private Boolean defaultCase;
	
	private String creator;
	
	/**是否匿名事件*/
	private boolean anonymous;
	
	private Date cts;
	
	/**是否相关人仅见*/
	private boolean hasOpen;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getMembers() {
		return members;
	}

	public void setMembers(String members) {
		this.members = members;
	}

	public byte[] getMessage() {
		return message;
	}

	public void setMessage(byte[] message) {
		this.message = message;
	}

	public Integer getTop() {
		return top;
	}

	public void setTop(Integer top) {
		this.top = top;
	}

	public Boolean getHasRead() {
		return hasRead;
	}

	public void setHasRead(Boolean hasRead) {
		this.hasRead = hasRead;
	}

	public Integer getNewPrise() {
		return newPrise;
	}

	public void setNewPrise(Integer newPrise) {
		this.newPrise = newPrise;
	}

	public Boolean getDefaultCase() {
		return defaultCase;
	}

	public void setDefaultCase(Boolean defaultCase) {
		this.defaultCase = defaultCase;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public boolean isAnonymous() {
		return anonymous;
	}

	public void setAnonymous(boolean anonymous) {
		this.anonymous = anonymous;
	}

	public Date getCts() {
		return cts;
	}

	public void setCts(Date cts) {
		this.cts = cts;
	}

	public boolean isHasOpen() {
		return hasOpen;
	}

	public void setHasOpen(boolean hasOpen) {
		this.hasOpen = hasOpen;
	}

	
}
